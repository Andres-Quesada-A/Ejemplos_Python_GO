# Declaración de una lista
lista_numeros = [1, 2, 3, 4, 5]
print(lista_numeros)  # Output: [1, 2, 3, 4, 5]

# Acceso a un elemento de la lista
primer_elemento = lista_numeros[0]
print(primer_elemento)  # Output: 1

# Cambio de valor de un elemento de la lista
lista_numeros[0] = 10
print(lista_numeros)  # Output: [10, 2, 3, 4, 5]

# Agregando un elemento a la lista
lista_numeros.append(6)
print(lista_numeros)  # Output: [10, 2, 3, 4, 5, 6]












# Declaración de una tupla
tupla_numeros = (1, 2, 3, 4, 5)
print(tupla_numeros)  # Output: (1, 2, 3, 4, 5)

# Acceso a un elemento de la tupla
primer_elemento = tupla_numeros[0]
print(primer_elemento)  # Output: 1











# Declaración de un diccionario
diccionario_numeros = {"uno": 1, "dos": 2, "tres": 3}
print(diccionario_numeros)  # Output: {"uno": 1, "dos": 2, "tres": 3}

# Acceso a un valor en el diccionario
valor_uno = diccionario_numeros["uno"]
print(valor_uno)  # Output: 1

# Agregando un elemento al diccionario
diccionario_numeros["cuatro"] = 4
print(diccionario_numeros)  # Output: {"uno": 1, "dos": 2, "tres": 3, "cuatro": 4}

