# Declaración de una variable entera
numero_entero = 5
print(numero_entero)  # Output: 5


# Declaración de una variable flotante
numero_flotante = 3.14
print(numero_flotante)  # Output: 3.14


# Declaración de una variable compleja
numero_complejo = 2 + 3j
print(numero_complejo)  # Output: (2+3j)


# Declaración de una variable de cadena de caracteres
cadena_caracteres = "Hola Mundo!"
print(cadena_caracteres)  # Output: Hola Mundo!


# Declaración de una variable booleana
booleano_verdadero = True
booleano_falso = False
print(booleano_verdadero)  # Output: True
print(booleano_falso)  # Output: False
