# Salto de línea (\n)
# La secuencia de escape "\n" se utiliza para representar un salto de línea en una cadena de caracteres. Por ejemplo:
# Ejemplo de secuencia de escape para salto de línea
print("Hola\nMundo")

# Este código imprimirá:
# Hola
# Mundo






# Tabulación (\t)
# La secuencia de escape "\t" se utiliza para representar una tabulación en una cadena de caracteres. Por ejemplo:
# Ejemplo de secuencia de escape para tabulación
print("Nombre\tEdad")
print("Juan\t25")
print("María\t30")


# Este código imprimirá:
# Nombre  Edad
# Juan    25
# María   30







# Comillas simples y dobles (', ")
# Las secuencias de escape "'" y """ se utilizan para representar comillas simples y dobles, respectivamente, dentro de una cadena de caracteres. Por ejemplo:
# Ejemplo de secuencia de escape para comillas simples y dobles
print("\"La mañana es una parte importante del día, ya que la forma en la que pasas sus horas te dicen cómo será el resto del día\" - Lemony Snicket")
print('\'Haz de cada día tu obra maestra.\' - John Wooden')


# Este código imprimirá:
# "La mañana es una parte importante del día, ya que la forma en la que pasas sus horas te dicen cómo será el resto del día" - Lemony Snicket
# 'Haz de cada día tu obra maestra.' - John Wooden







# Caracteres Unicode (\uXXXX)
# La secuencia de escape "\uXXXX" se utiliza para representar un carácter Unicode en una cadena de caracteres, donde XXXX es el número hexadecimal correspondiente al carácter Unicode. Por ejemplo:
# Ejemplo de secuencia de escape para caracteres Unicode
print("El símbolo del euro es: \u20AC")


# Este código imprimirá:
# El símbolo del euro es: €