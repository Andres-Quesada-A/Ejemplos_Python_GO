# Estructura de control
# Definir una lista de números
numeros = [1, 2, 3, 4, 5]

# Inicializar una variable para almacenar la suma
suma = 0

# Recorrer la lista de números
for numero in numeros:
    # Sumar el número actual a la suma total
    suma += numero

# Imprimir la suma total
print("La suma de los números es:", suma)








# Funciones
# Definir una función que tome dos argumentos
def multiplicar(x, y):
    # Multiplicar los dos argumentos y almacenar el resultado en una variable
    resultado = x * y
    # Imprimir el resultado
    print("El resultado de la multiplicación es:", resultado)

# Llamar a la función con dos argumentos
multiplicar(5, 10)










# Modificación de variables
# Definir una variable inicial
numero = 10

# Añadir 5 al número actual
numero += 5

# Multiplicar el número actual por 2
numero *= 2

# Restar 3 al número actual
numero -= 3

# Imprimir el resultado final
print("El resultado es:", numero)