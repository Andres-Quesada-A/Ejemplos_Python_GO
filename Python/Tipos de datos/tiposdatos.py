# Declaración de un número entero
numero_entero = 5
print(numero_entero)  # Output: 5



# Declaración de un número flotante
numero_flotante = 3.14
print(numero_flotante)  # Output: 3.14



# Declaración de un número complejo
numero_complejo = 2 + 3j
print(numero_complejo)  # Output: (2+3j)



# Declaración de una cadena de caracteres
cadena_caracteres = "Hola Mundo!"
print(cadena_caracteres)  # Output: Hola Mundo!



# Declaración de una lista
lista_numeros = [1, 2, 3, 4, 5]
print(lista_numeros)  # Output: [1, 2, 3, 4, 5]



# Declaración de una tupla
tupla_numeros = (1, 2, 3, 4, 5)
print(tupla_numeros)  # Output: (1, 2, 3, 4, 5)


# Declaración de un conjunto
conjunto_numeros = {1, 2, 3, 4, 5}
print(conjunto_numeros)  # Output: {1, 2, 3, 4, 5}



# Declaración de un diccionario
diccionario_numeros = {"uno": 1, "dos": 2, "tres": 3}
print(diccionario_numeros)  # Output: {"uno": 1, "dos": 2, "tres": 3}



